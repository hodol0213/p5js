function setup() {
  createCanvas(600, 400);
  rectMode(CORNER); // 직사각형 기준점을 좌측 상단으로
}

function draw() {
  // ----- 배경 (하늘) -----
  background(135, 206, 235); // 연한 하늘색

  // ----- 해 -----
  noStroke();
  fill(255, 204, 0);
  circle(500, 80, 80); // 큰 해

  // ----- 산 -----
  fill(34, 139, 34);
  triangle(100, 300, 250, 120, 400, 300); // 왼쪽 산
  triangle(300, 300, 450, 150, 600, 300); // 오른쪽 산

  // ----- 땅 -----
  fill(50, 205, 50);
  rect(0, 300, 600, 100); // 땅 영역

  // ----- 길 -----
  fill(139, 69, 19);
  quad(250, 300, 350, 300, 400, 400, 200, 400);

  // ----- 나무 (왼쪽) -----
  // 나무 줄기
  fill(101, 67, 33);
  rect(80, 250, 20, 50);
  // 나무 잎 (동그라미 3개)
  fill(0, 128, 0);
  circle(70, 250, 40);
  circle(90, 230, 40);
  circle(110, 250, 40);

  // ----- 나무 (오른쪽) -----
  fill(101, 67, 33);
  rect(480, 250, 20, 50);
  fill(0, 128, 0);
  circle(470, 250, 40);
  circle(490, 230, 40);
  circle(510, 250, 40);

  // ----- 작은 디테일: 태양 광선 -----
  stroke(255, 204, 0);
  strokeWeight(2);
  line(500, 30, 500, 10);
  line(540, 50, 560, 30);
  line(550, 80, 570, 80);
  line(540, 110, 560, 130);
  line(500, 130, 500, 150);
  line(460, 110, 440, 130);
  line(430, 80, 450, 80);
  line(460, 50, 440, 30);

  noLoop(); // 정지 (애니메이션 필요 없음)
}
